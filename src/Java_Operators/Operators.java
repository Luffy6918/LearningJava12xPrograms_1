package Java_Operators;

public class Operators {

    public static void main(String[] args) {
        int a=10;
        int b=20;
        boolean c =a>b;

        System.out.println(c);
        System.out.println(a+b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println(a!=b);
        System.out.println(a>b);
        boolean d= a>b;
        boolean e=a<b;
        System.out.println(d||e);
        System.out.println(d&&e);
        System.out.println(d==e);



    }
}
